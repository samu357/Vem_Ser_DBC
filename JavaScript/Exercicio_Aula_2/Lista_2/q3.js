var tabuada = parseInt(prompt('Escolha um numero de 1 a 10'))
for (let i = 1; i <= 10; i++) {
  console.log(`multição ${tabuada}*${i} = ${tabuada * i}`)
  console.log(`soma ${tabuada}+${i} = ${tabuada + i}`)
  console.log(`subtração ${tabuada}-${i} = ${tabuada - i}`)
  console.log(`divisao ${tabuada}/${i} = ${tabuada / i}`)
}
